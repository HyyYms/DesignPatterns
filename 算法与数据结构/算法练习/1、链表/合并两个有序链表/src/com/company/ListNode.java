package com.company;

/**
 * @program: 合并两个有序链表
 * @description:
 * @author: Mr.Huang
 * @create: 2020-11-16 23:12
 **/

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
